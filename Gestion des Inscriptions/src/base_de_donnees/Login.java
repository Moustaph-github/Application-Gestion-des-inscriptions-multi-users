    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        rs = db.querySelectAll("utilisateur", "username='" +txt_username.getText()
        + "' and password='" +txt_password.getText()+ "'");
        
        try {
            while(rs.next()){
                username1 = rs.getString("username");
                password1 = rs.getString("password");
                hak = rs.getString("type");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(username1 == null && password1 == null){
            JOptionPane.showMessageDialog(this, "le nom utilisateur ou le mot de passe est incorrect");
            cptTentaiveConnexion++;
            if(cptTentaiveConnexion >= 3){
                this.dispose();
            }
            
        } else {
            if(hak.equals("administrateur")){
                MenuAdministrateur mad = new MenuAdministrateur();
                mad.setVisible(true);
                this.dispose();// Si on a l'administrateur le programme ouvre la page MenuAdministrateur 
            } else if(hak.equals("coordonnateur")){
                MenuCoordonnateur mc = new MenuCoordonnateur();
                mc.setVisible(true);
                this.dispose();
            } else if(hak.equals("assistant")){
                MenuAssistant mas = new MenuAssistant();
                mas.setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

