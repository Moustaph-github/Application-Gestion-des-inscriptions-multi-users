    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(txt_id_etud.getText().equals("") || txt_annee.getText().equals("") || com_mention.getSelectedItem().equals("Mention") || txt_etablissement.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "SVP entrez les informations complétes");
        } else {
            try {
                
                while(rs1.next()){
                    idetudiant = rs1.getString("identifiant");
                    
                    if(txt_id_etud.getText().equalsIgnoreCase(idetudiant))
                    {
                        String[] colon = {"id_etudiant", "annee", "mention", "etablissement"};
                        String[] inf = {txt_id_etud.getText(), txt_annee.getText(), com_mention.getSelectedItem().toString(), txt_etablissement.getText()};
                        System.out.println(db.queryInsert("diplome", colon, inf));
                        table();
                        actualiser();
                        JOptionPane.showMessageDialog(this, "Diplome enregistré avec succés");
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Cet étudiant n'existe pas");   
                    }
                    
                }
                
                
            } catch (Exception e) {
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

