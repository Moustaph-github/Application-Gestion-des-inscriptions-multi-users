    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        if(txt_rech.getText().equals("")){
            JOptionPane.showMessageDialog(this, "SVP entrez quelque chose");
        } else {
            if(com_rech.getSelectedItem().equals("id_diplome")){
                rs = db.querySelectAll("diplome", "id_diplome LIKE '%" +txt_rech.getText()+ "%'");
                table_diplome.setModel(new ResultSetTableModel(rs));

            } else if(com_rech.getSelectedItem().equals("id_etudiant")){
                rs = db.querySelectAll("diplome", "diplome LIKE '%" +txt_rech.getText()+ "%'");
                table_diplome.setModel(new ResultSetTableModel(rs));

            } else if(com_rech.getSelectedItem().equals("annee")){
                rs = db.querySelectAll("diplome", "annee LIKE '%" +txt_rech.getText()+ "%'");
                table_diplome.setModel(new ResultSetTableModel(rs));

            } else if(com_rech.getSelectedItem().equals("mention")){
                rs = db.querySelectAll("diplome", "mention LIKE '%" +txt_rech.getText()+ "%'");
                table_diplome.setModel(new ResultSetTableModel(rs));

            } else if(com_rech.getSelectedItem().equals("etablissement")){
                rs = db.querySelectAll("diplome", "etablissement LIKE '%" +txt_rech.getText()+ "%'");
                table_diplome.setModel(new ResultSetTableModel(rs));
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

