    public void table(){
        String a[] = {"etudiant.identifiant","diplome.id_diplome", "contact.id_contact", "etudiant.prenom", "etudiant.nom", "etudiant.annee", "etudiant.departement", "diplome.annee", "diplome.mention", "diplome.etablissement",
                        "contact.prenom_contact", "contact.nom_contact"};
        
        
        rs1 = db.querySelectAll("etudiant");
        rs2 = db.querySelectAll("diplome");
        rs3 = db.querySelectAll("contact");
        
        try {
            
            while(rs1.next() && rs2.next() && rs3.next()){
                identifiant1 = rs1.getString("identifiant");
                System.out.println(identifiant1);
                
                id_etudiant = rs2.getString("id_etudiant");
                System.out.println(id_etudiant);
                
                id_student = rs3.getString("id_student");
                System.out.println(id_student);
                    
                etat = " FROM etudiant INNER JOIN diplome ON etudiant.identifiant LIKE'%" +id_etudiant+ "%'INNER JOIN contact ON etudiant.identifiant LIKE'%" +id_student+ "%'";
                
                if(identifiant1.equalsIgnoreCase(id_etudiant) && identifiant1.equalsIgnoreCase(id_student)){
                    
                    rs = db.querySelectIns(a,etat);
                    table_inscription.setModel(new ResultSetTableModel(rs));
                    i++;        
                }
        
                 
            }
            
            rs1.close();
            rs2.close();
            rs3.close();
                
        } catch (SQLException e) {
            System.err.println(e.getMessage());  
        }
       
    }

