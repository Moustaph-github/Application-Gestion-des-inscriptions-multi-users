    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        if(txt_rech.getText().equals("")){
            JOptionPane.showMessageDialog(this, "SVP entrez quelque chose");
        } else {
            if(com_rech.getSelectedItem().equals("genre")){
                rs = db.querySelectAll("etudiant", "genre LIKE '%" +txt_rech.getText()+ "%'");
                table_student.setModel(new ResultSetTableModel(rs));

            } else if(com_rech.getSelectedItem().equals("emploi")){
                rs = db.querySelectAll("etudiant", "emploi LIKE '%" +txt_rech.getText()+ "%'");
                table_student.setModel(new ResultSetTableModel(rs));

            } else if(com_rech.getSelectedItem().equals("statut")){
                rs = db.querySelectAll("etudiant", "statut LIKE '%" +txt_rech.getText()+ "%'");
                table_student.setModel(new ResultSetTableModel(rs));
            } 
        }
    }//GEN-LAST:event_jButton5ActionPerformed

